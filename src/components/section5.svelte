        <p>Ed Durr stood before the audience in Shamong and issued a stark warning.</p>
        <p>“They are coming for your kids,” he said.</p>
        <p>The “they”? Democrats.</p>
        <p>Durr, a former state senator, was referring to Gov. Phil Murphy’s 2023 executive order allowing people of all ages access to transition surgery and hormone treatments.</p>
        <p>“These little incremental things that they do with the age … they say we’re going to go to 16, then we’re going to go to 14, then we’re going to go to 10,” Durr said fiercely. “They want to do away with age. Why? We all know why.</p>
        <p>“They’re grooming. That’s the bottom line.”</p>
        <p>Durr knew his audience. He drove them to their feet.</p>
        <p>He had been a feel-good story in South Jersey, spending just $2,500 to win the state Senate primary and upset Sweeney in 2021. But soon after, he was apologizing for previous social media messages, such as “A woman does have a choice! Keep her legs closed.” and "Mohammed was a pedophile! Islam is a false religion!”</p>
        <p>Now he’s running for governor.</p>
