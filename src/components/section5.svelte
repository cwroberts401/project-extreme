        <p>More far-right candidates like Durr might soon be coming to New Jersey, experts say.</p>
        <p>The long-held county-line system — allowing party chairs to give candidates preferential placement on primary election ballots — is expected to be ruled unconstitutional after it was challenged in the 2023 Democratic Senate primary. Grassroots groups (right and left) view it as a victory for democracy, removing an enormous advantage fueled by favoritism and patronage.</p>
        <p>But the county-line system also keeps “extreme candidates” from gaining political power, according to Rasmussen, the Rebovich Institute director.</p>
        <p>“I think it has been used for the promotion of less extreme candidates,” he said.</p>
        <p>Many political observers now fear town and county organizations will be “taken over by left-wing radicals and right-wing radicals,” said James Santonastaso, a Democratic political operative in Sussex County.</p>
        <p>“You mark my words, in two years, three years, four years, New Jersey will be dangerously close to going red in the Legislature because of the loss of the line,” he said.</p>
        <p>But even now, figures like Durr and Crispi are far from isolated, fringe voices despite spouting hate.</p>
        <p>Crispi has allies throughout the state like Auth, a conservative who avoids extremist rhetoric and sells himself as “promoting common sense voices in” New Jersey. Yet they often appear together at events.</p>
        <p>Why would state and local leaders align themselves with figures like Crispi, Durr and Ingrassia?</p>
        <p>“Right now (Crispi is) in communication with a segment of the Republican coalition,” Rasmussen said.</p>
        <p>“And he's clearly got inroads within that community,” he added.</p>
        <p>On the other side, Auth “must feel — and there's probably some evidence to support this — ‘My district is more conservative than most … I can put together a coalition with these folks,” Rasmussen said.</p>
        <p>Auth, who did not return messages seeking comment, is hardly alone.</p>
        <p>Santos is among the politicians walking a fine line: tuning out the far right’s divisive messaging while embracing its support. The chair of the Union County Republican Party — ousting the establishment chair in June — Santos welcomed an endorsement from Ingrassia at a February fundraiser co-hosted by the America First Republicans of New Jersey.</p>
        <p>"For me, a lot of that stuff I don't pay attention to," Santos told NJ Advance Media about America First’s messaging. He added that he’s "all in on Trump,” but is not an “extreme MAGA guy” or a “conspiracy theory guy.”</p>
        <p>I attended two Santos events — one in February and another in April, when he appeared with Spadea. Santos didn’t spout conspiracy theories or white nationalist rhetoric. In fact, he’s somewhat shy, especially when he speaks before large crowds.</p>
        <p>And he sounds like a conventional candidate, usually talking about his wish to get more people involved in politics.</p>
        <p>But in trying to build a coalition, he has aligned himself with individuals espousing white nationalist views.</p>
        <p>On his website, Santos supports Mack’s Concerned Citizens of New Jersey. The CCNJ website is filled with Russian State TV propaganda and conspiracy theory videos about vaccines, global censorship, the Maui wildfires and one claiming “Your Government Hates You and Is Destroying You to Benefit China.” Another video, no longer on the site, was titled: “Democrats Are Preparing to Call for Mass Executions of All Christians and Conservatives.”</p>
        <p>"I don't agree with all that,” Santos said about Mack's website and other grassroots groups he promotes. “I do have them on the site because they're grassroots groups that come out and support me and everything, and I just put their stuff up as well."</p>
        <p>Mack hosted a movie night in April for members of the Gateway Church in Bridgewater. Just a few weeks earlier, Maxwell, the leader of that church, gave an antisemitic sermon about Israel and a globalist Jewish conspiracy.</p>
        <p>“There is a huge psychological war that's going on using words, and we have become aware that we've been lied to us about many things, just about everything, divided by all kinds of misinformation,” Maxwell said. “And the main culprit is the mainstream media controlled by the globalist Kazarian Jews. They have controlled the narrative for centuries longer than we realize.”</p>
        <p>But Santos doesn’t want to allow such inconvenient ties to get in the way of what he’s building.</p>
        <p>He is trying to build a coalition in solidly blue Union County. He’s assembled fledgling bases in Elizabeth and Linden where none existed and is growing others in Hillside, Rahway and Roselle, he says.</p>
        <p>"I'm more worried about building the party, growing, winning elections on a local level and just giving Republicans a voice again, especially in the underserved Republican areas,” he said.</p>
