      <p>The man wore a black mask as he pulled in front of an Asbury Park church.</p>

      <p>He watched as a crowd of nearly 200 began heading home that January night in 2023 just as an anti-racism concert ended.</p>
      
      <p>But Nicholas Mucci didn’t want them to go. Not yet. He tried to block their exit with his Honda CR-V, authorities allege. Then he tossed two smoke bombs, filling the street in front of Trinity Episcopal Church with smoke.</p>
      
      <p>“White lives matter, too,” he yelled before speeding away.</p>
      
      <p>Mucci returned an hour later.</p>
      
      <p>The 28-year-old man raced out of the SUV and attempted to bear spray those still gathered in the church parking lot, according to officials.</p>
      
      <p>“White lives matter,” he yelled again before fleeing a second time.</p>
      
      <p>After an investigation, Mucci — a white supremacist from Toms River — was arrested.</p>
      
      <p>Then a chilling discovery was made. When authorities searched his home, they found an unregistered AR-style assault weapon with a bump stock, a homemade bomb, and large-capacity ammunition magazines, according to court documents.</p>
      
      <p>And it turns out, law enforcement had already been looking for him for months. He was a suspect behind a racist banner hung over a Route 287 overpass in Edison.</p>
      
      <p>“It all started with this banner,” Tom Hauck, director of intelligence and operations at the New Jersey Office of Homeland Security and Preparedness, told NJ Advance Media.</p>
      
      <p>The banner, hung in October 2022, carried white supremacist messaging, including “White Power.”</p>
      
      <p>“They just started pulling threads,” Hauck said. “And fast forward, now we have this guy attacking the concert at a church, blocking people from leaving, throwing smoke and bear spray.”</p>
      
      <p>Mucci pleaded guilty in September to causing or risking widespread damage and unlawful possession of an assault firearm. He was sentenced to eight years in state prison.</p>
      
      <p>“I'm sorry that I added to the divisiveness — the division in this country,” he said at his sentencing hearing.</p>
      
